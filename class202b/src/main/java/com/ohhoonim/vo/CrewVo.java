package com.ohhoonim.vo;

public class CrewVo {
	private String crewId;
	private String crewNm;
	private String crewLoc;
	private String crewCont;
	private String del;
	
	public String getCrewId() {
		return crewId;
	}
	public void setCrewId(String crewId) {
		this.crewId = crewId;
	}
	
	public String getCrewNm() {
		return crewNm;
	}
	public void setCrewNm(String crewNm) {
		this.crewNm = crewNm;
	}
	
	public String getCrewLoc() {
		return crewLoc;
	}
	public void setCrewLoc(String crewLoc) {
		this.crewLoc = crewLoc;
	}
	
	public String getCrewCont() {
		return crewCont;
	}
	public void setCrewCont(String crewCont) {
		this.crewCont = crewCont;
	}
	
	public String getDel() {
		return del;
	}
	public void setDel(String del) {
		this.del = del;
	};
	
	
	
}

package com.ohhoonim.vo;

public class CodeVo {
	private String mainCode;
	private String mainCodeNm;
	private String subCode;
	private String subCodeNm;
	private String used;
	
	
	public String getMainCode() {
		return mainCode;
	}
	public void setMainCode(String mainCode) {
		this.mainCode = mainCode;
	}
	
	
	public String getMainCodeNm() {
		return mainCodeNm;
	}
	public void setMainCodeNm(String mainCodeNm) {
		this.mainCodeNm = mainCodeNm;
	}
	
	
	public String getSubCode() {
		return subCode;
	}
	public void setSubCode(String subCode) {
		this.subCode = subCode;
	}
	
	
	public String getSubCodeNm() {
		return subCodeNm;
	}
	public void setSubCodeNm(String subCodeNm) {
		this.subCodeNm = subCodeNm;
	}
	
	
	public String getUsed() {
		return used;
	}
	public void setUsed(String used) {
		this.used = used;
	}
}
